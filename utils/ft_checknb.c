#include "push_swap.h"

int	ft_checknb(char *nb)
{
	int	i;

	i = 0;
	while (nb[i] == '\t' || nb[i] == '\n' || nb[i] == '\r' || nb[i] == '\v'
		|| nb[i] == '\f' || nb[i] == ' ')
		i++;
	if (nb[i] == '-' || nb[i] == '+')
		i++;
	if (nb[i] == '\0')
		return (1);
	while (nb[i])
	{
		if (!ft_isdigit(nb[i]))
			return (1);
		i++;
	}
	return (0);
}
